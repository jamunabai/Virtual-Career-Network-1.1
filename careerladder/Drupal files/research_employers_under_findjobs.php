				<tr>
					<td>
						<p>
							<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">What products/services does the employer provide?</span></span></p>
						<p>
							&nbsp;</p>
						<p>
							&nbsp;</p>
						<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">What industry trends are affecting the company?</span></span></td>
					<td style="text-align: left;">
						<p>
							<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><strong>Employer Web sites.</strong> These often describe of the company unit(s) that may be hiring in your field. They also provide details on the types of products or programs they offer.</span></span></p>
						<p>
							<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><strong>Regional or state publications</strong>:</span></span></p>
						<ul>
							<li>
								<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Chamber of Commerce directories</span></span></li>
							<li>
								<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Manufacturers&#39; directories by state</span></span></li>
						</ul>
						<p>
							<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><strong>National publications</strong>. Most libraries have copies in their business sections.</span></span></p>
						<ul>
							<li>
								<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><em>National Trade &amp; Professional Associations</em></span></span></li>
							<li>
								<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><em>Standard and Poor&#39;s Register of Corporations, Directors &amp; Executives</em></span></span></li>
							<li>
								<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><em>The Macmillan Directory of Leading Private Companies</em></span></span></li>
							<li>
								<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><em>Ward&#39;s Business Directory of Major U.S. Private Companies</em></span></span></li>
						</ul>
						<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">The titles above are just a few examples. Almost every type of field or industry has its own trade association.</span></span></td>
				</tr>
				<tr>
					<td>
						<p>
							<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">How many employees does the employer have?</span></span></p>
						<p>
							<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">What jobs does the company typically hire for?</span></span></p>
						<p>
							<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">What is the employer&#39;s hiring policy?</span></span></p>
						<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Where is the employer located? Does it have more than one location? Is it on the bus line?</span></span></td>
					<td>
						<p>
							<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><strong>Employer Web sites.</strong> These often include size of their workforce, hiring policies, and job openings. It may also provide details on company location(s) and mass transit options. Also check recent press releases for news about plant expansions, new product roll-outs, or sponsorship of events.</span></span></p>
						<p>
							&nbsp;</p>
						<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><strong>College and university</strong> <strong>placement offices.</strong> Check if the company is recruiting on campus. Find out which qualifications they look for when they hire.</span></span></td>
				</tr>
				<tr>
					<td>
						<p>
							<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">What are the employer&#39;s mission and values?</span></span></p>
						<p>
							<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">How long have they been in business?</span></span></p>
						<p>
							<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Is the employer involved in community services? If so, what?</span></span></p>
					</td>
					<td>
						<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><strong>Employer Web sites.</strong> Look for annual reports. Or contact the employer&#39;s public relations office or personnel department. Annual reports outline the employer&#39;s successes, growth, history, and financial status. Companies that give back to the community often have a section of their Web site devoted their efforts.</span></span></td>
				</tr>
				<tr>
					<td>
						<p>
							<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">What is the employer&#39;s financial situation? Is it making money? Has it downsized in the last five years?</span></span></p>
						<p>
							<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Has there been any recent important news related to the company?</span></span></p>
						<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">What do others think about the company?</span></span></td>
					<td>
						<p>
							<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><strong>Employer Web sites.</strong> Review recent and past press releases. Annual reports often include a financial report for the year. If the company has investors, detailed financial reports may also be online.</span></span></p>
						<p>
							&nbsp;</p>
						<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><strong>Current newspapers, trade journals, and business magazines.</strong> The business sections of most papers have articles about local companies and their executives. You can find back copies in library archives. Also check <a href="http://www.businessweek.com/" target="_blank">BusinessWeek</a> for company and industry news.</span></span></td>
				</tr>
				<tr>
					<td>
						<p>
							<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">What&#39;s it really like to work for this employer?</span></span></p>
					</td>
					<td>
						<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><strong>Current or past employees. </strong>Talk with people who are working or have worked for the company.</span></span></td>
