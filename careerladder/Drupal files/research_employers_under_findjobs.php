						<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><span style="color: rgb(255, 255, 255);"><strong>Question</strong></span></span></span></th>
					<th scope="col" style="background-color: rgb(24, 154, 176);">
						<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><span style="color: rgb(255, 255, 255);"><strong>Where to Search</strong></span></span></span></th>
				</tr>
