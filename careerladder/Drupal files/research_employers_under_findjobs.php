			<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Research is a critical part of the job search process. It can help you:</span></span></p>
		<ul>
			<li>
				<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Identify companies that may be hiring in your field</span></span></li>
			<li>
				<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Focus your job search on employers whose values and expectations meet yours</span></span></li>
			<li>
				<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Learn about potential growth of businesses and industries</span></span></li>
			<li>
				<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Prepare you for a job interview</span></span></li>
			<li>
				<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Understand how you can apply your skills in a new field</span></span></li>
		</ul>
		<p>
			<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><strong>Identify companies</strong></span></span></p>
		<p>
			<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Visit CareerOneStop&rsquo;s <a href="http://www.careerinfonet.org/employerlocator/employerlocator.asp?nodeid=18" target="_blank">Employer Locator</a> to identify and get contact information for potential employers in your local area.</span></span></p>
		<p>
			<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><strong>Do your research</strong></span></span></p>
		<p>
			<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Once you&rsquo;ve identified some companies you&rsquo;re interested in or perhaps even scheduled an interview you can visit their Web sites to learn more about them.&nbsp; Locate Web sites by entering the full name of the business into a search engine, such as Google or Yahoo. Below are some key questions you might ask about potential employers:</span></span></p>
		<p>
			&nbsp;</p>
