				<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Looking for a job? Your first impulse may be to grab the help-wanted section or surf the Web for job ads. But your job search will be more effective if you first take the time to create a plan.</span></span></p>
			<p>
				<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Make a weekly or daily schedule of job search activities. This plan will help keep you track during your search. Develop your own schedule by committing to at least some of the following activities:</span></span></p>
