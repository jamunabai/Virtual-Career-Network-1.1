				<li>
					<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Learn about careers, including required training and education and average salaries, at <a href="http://www.careeronestop.org/" target="_blank">CareerOneStop</a>.</span></span></li>
				<li>
					<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Set up informational interviews</span></span></li>
