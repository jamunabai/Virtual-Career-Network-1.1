				<li>
					<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Find and contact your local <a href="http://www.servicelocator.org/" target="_blank">One-Stop Career Center</a> to talk to with a counselor and learn what specific resources might be available to help in your search</span></span></li>
				<li>
					<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Take assessments to identify your skills, interests, values, or other traits. Ask a counselor or search online for &quot;free career assessments&quot;</span></span></li>
				<li>
					<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Prepare your resume. </span></span></li>
				<li>
					<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Get in touch with your networking contacts </span></span></li>
				<li>
					<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Attend support groups and job clubs in your area</span></span></li>
				<li>
					<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Attend job search training sessions or related training</span></span></li>
			</ul>
			<p>
				<span style="color: rgb(178, 34, 34);"><span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><strong>Explore career options</strong></span></span></span></p>
