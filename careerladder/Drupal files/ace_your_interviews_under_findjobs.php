			<span style="color: rgb(178, 34, 34);"><span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><strong>Interview Tips</strong></span></span></span></p>
		<p>
			<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Interviews are your chance to sell your skills and abilities. They also give you a chance to find out if the job and company are right for you. Follow the tips here to ace your interviews.</span></span></p>
		<p>
			<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><span style="color: rgb(178, 34, 34);"><strong>Research the employer and the position. </strong></span>You can learn a lot from the job description, employer and industry websites, annual business reports, trade periodicals, and local and state employment service offices.</span></span></p>
		<p>
			<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><span style="color: rgb(178, 34, 34);"><strong>Review common interview questions.</strong></span> You can find examples of these questions by searching on line for &ldquo;common job interview questions.&rdquo; Practice answering them with someone else or in front of a mirror. Come prepared with stories that relate to the skills that the employer wants, while emphasizing your:</span></span></p>
		<ul>
			<li>
				<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Strengths</span></span></li>
			<li>
				<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Willingness to work and flexibility</span></span></li>
			<li>
				<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Leadership skills</span></span></li>
			<li>
				<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Ability and willingness to learn new things</span></span></li>
			<li>
				<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Contributions to the organizations in which you have worked or volunteered</span></span></li>
			<li>
				<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Creativity in solving problems and working with people</span></span></li>
		</ul>
		<p>
			<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;"><span style="color: rgb(165, 42, 42);"><strong>Conduct a professional interview. </strong></span>On the big day, remember to:</span></span></p>
		<ul>
			<li>
				<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Be on time or early</span></span></li>
			<li>
				<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Go by yourself</span></span></li>
			<li>
				<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Look professional</span></span></li>
			<li>
				<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Shake hands firmly</span></span></li>
			<li>
				<span style="font-size: 14px;"><span style="font-family: verdana,geneva,sans-serif;">Maintain eye contact</span></span></li>
		</ul>
