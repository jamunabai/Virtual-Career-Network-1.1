The purpose of this platform is to assist new entrants and un- and under-employed persons transition 
into high paying, high growth healthcare careers.  The VCN will offer a structured step-by-step online process designed to assist users-- 
</td></tr>

<tr><td>&nbsp;</td></tr>

<tr><td colspan="2">
<ul>
  <li> 	To determine whether a career in healthcare is appropriate for them by linking to labor market information and experiential information, such as career videos or 'day-in-the-life stories', to answer common user questions ("what do jobs like this pay?", "Is this a growing occupation?", "What is it like to be a ...", etc.);</li><br />
  <li> 	To decide which healthcare career is the best fit for them; </li><br />
  <li> 	To identify the credential(s) needed to get and hold a job in the career or career path they choose;</li><br />
  <li> 	To locate an appropriate instructional program, either online or at a local community college, completion of which would lead to obtaining that credential;</li><br />
  <li> 	To receive the developmental education (directly through the platform) where online assessments determine that additional academic preparation is required to meet the prerequisites for entry into that school and/or instructional program;</li><br />
  <li>  To find and apply for financial aid to help cover costs of taking such an instructional program; and</li><br />
  <li> 	To create and maintain a personal Career Management Account in which they can store all of this information (and more), maintain personal control over their academic, counseling, and work records, and (over time) direct their own career development.</li><br />
</ul>
</td></tr>

<tr><td colspan="2">
